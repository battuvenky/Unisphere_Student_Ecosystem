import { randomUUID } from "crypto";
import path from "path";
import { listTasks } from "@/lib/tasks-store";
import { loadStore, saveStore } from "@/lib/mongo-store";

export type ScheduleCategory = "class" | "study" | "exam" | "event";

export type ScheduleBlockRecord = {
  id: string;
  userId: string;
  title: string;
  subject: string;
  category: ScheduleCategory;
  dayOfWeek: number;
  startTime: string;
  endTime: string;
  location: string;
  autoGenerated: boolean;
  createdAt: string;
  updatedAt: string;
};

type SchedulerStore = {
  blocks: ScheduleBlockRecord[];
};

const dataDir = path.join(process.cwd(), "data");
const schedulerFile = path.join(dataDir, "scheduler.json");

const dayLabels = ["Sunday", "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday"];

function nowIso() {
  return new Date().toISOString();
}

function userHash(userId: string) {
  return userId.split("").reduce((sum, char) => sum + char.charCodeAt(0), 0);
}

function parseTimeToMinutes(time: string) {
  const match = time.match(/^(\d{2}):(\d{2})$/);
  if (!match) {
    return null;
  }

  const hours = Number(match[1]);
  const minutes = Number(match[2]);

  if (!Number.isInteger(hours) || !Number.isInteger(minutes)) {
    return null;
  }

  if (hours < 0 || hours > 23 || minutes < 0 || minutes > 59) {
    return null;
  }

  return hours * 60 + minutes;
}

function minutesToTime(totalMinutes: number) {
  const hours = Math.floor(totalMinutes / 60)
    .toString()
    .padStart(2, "0");
  const minutes = (totalMinutes % 60).toString().padStart(2, "0");
  return `${hours}:${minutes}`;
}

function clampDayOfWeek(dayOfWeek: number) {
  if (dayOfWeek < 0) {
    return 0;
  }

  if (dayOfWeek > 6) {
    return 6;
  }

  return Math.floor(dayOfWeek);
}

function getWeekdayFromDateString(value: string) {
  const parsed = Date.parse(value);
  if (Number.isNaN(parsed)) {
    return null;
  }

  return new Date(parsed).getDay();
}

async function readStore(): Promise<SchedulerStore> {
  return loadStore<SchedulerStore>({
    collectionName: "scheduler",
    legacyFilePath: schedulerFile,
    initialValue: { blocks: [] },
  });
}

async function writeStore(store: SchedulerStore) {
  await saveStore({
    collectionName: "scheduler",
    legacyFilePath: schedulerFile,
    value: store,
  });
}

